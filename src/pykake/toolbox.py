def sum(x, y):
    '''
    Calculates the sum of x and y

    Parameters
    ----------
    x: float
        first number
    y: float
        second number
    
    Return
    ------
    float
        Sum of x and y
    '''
    return x + y

def mean(x, y):
    '''
    Calculates the mean of x and y

    Parameters
    ----------
    x: float
        first number
    y: float
        second number
    
    Return
    ------
    float
        Mean of x and y
    '''
    return (x + y) / 2
